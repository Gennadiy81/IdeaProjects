public class EmployeeInfo {
    public static void main(String[] args){
        System.out.println("Company name:\tGoogle");
        System.out.println("Employee:\t\tGennadiy");
        System.out.println("ID\t\t\t\t12345");
        System.out.println("Job Title\t\tSDET");
        System.out.println();
        System.out.println("Company name:\tGoogle\nEmployee:\t\tGennadiy\nID\t\t\t\t12345\nJob Title\t\tSDET");
    }
}
