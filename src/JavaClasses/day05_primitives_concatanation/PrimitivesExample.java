package JavaClasses.day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args) {
//******** WHOLE NUMBERS/INTEGER TYPES *******
      byte byteValue = 127;
     short shortValue = 32000;
     int intValue = 2121445453;
     long longValue = 5668677837876788760L;
     //***** DECIMAL NUMBERS/FLOATING ******
        float floatValue = 64.877F;
        double doubleValue = 63457347.5463457D; //D is optional
        //***** CHARACTER TYPE ****
        char charValue = 'y'; //LEST LATER OF MY NAME
        //***** BOOLEAN = true|false *****
        boolean booleanValue = true; // TRUE OF FALSE

       //PRINT VARIABLE VALUES
       System.out.println(byteValue);
       System.out.println(shortValue);
       System.out.println(intValue);
       System.out.println(longValue);
       System.out.println(floatValue);
       System.out.println(doubleValue);
       System.out.println(charValue);
       System.out.println(booleanValue);


    }
}
