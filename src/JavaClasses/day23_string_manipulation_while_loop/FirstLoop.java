package JavaClasses.day23_string_manipulation_while_loop;

public class FirstLoop {
    public static void main(String[] args) {
        double i = 1;
        while (i <= 5){
            System.out.println(i);
            i++;
        }

        int apples = 0;
        while (apples <= 3){
            System.out.println("apples -> " + apples);
            apples++;
        }
        System.out.println("apples = " + apples);

        while(true){
            System.out.println("hello");

        }
    }
}
