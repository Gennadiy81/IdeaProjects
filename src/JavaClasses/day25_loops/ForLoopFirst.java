package JavaClasses.day25_loops;

public class ForLoopFirst {
    public static void main(String[] args) {
        for (int i = 0; i <= 5; i++) {
            System.out.println("Hello");
        }
        //for loop: 1 - 10. print the numbers
        //  1)         2)       4)
        for (int i = 1; i <= 10; i++) {
            System.out.println(i); //3)
        }
    }
}
