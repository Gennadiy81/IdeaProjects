package OfficeHours.Practice_03_03_2021;

public class AgeToDay {
    public static void main(String[] args){
        int ageInYear = 20, daysInYear = 365,
        ageInDays = ageInYear * daysInYear;
        System.out.println("You are " + ageInYear +" years old, " + " the means you are " +
                ageInDays + " days old.");


    }
}
