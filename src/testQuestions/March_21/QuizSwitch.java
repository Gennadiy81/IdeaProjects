package testQuestions.March_21;

public class QuizSwitch {
    public static void main(String[] args) {
        int z = 11;
        switch (z){
            case 10:
                System.out.println("Monday");
            case 11:
                System.out.println("Tuesday");
            case 12:
                System.out.println("Wednesday");
            default:
                System.out.println("Friday");
        }
    }
}
